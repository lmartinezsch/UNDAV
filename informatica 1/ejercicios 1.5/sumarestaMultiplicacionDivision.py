def main():
    nroUno = int(input('Ingrese un nuemero: '))
    nroDos = int(input('Ingrese otro numero: '))
    suma = nroUno + nroDos
    resta = nroUno - nroDos
    multiplicacion = nroUno * nroDos
    division = nroUno / nroDos
    print('La suma es: ', suma)
    print('La resta es: ', resta)
    print('La multiplicacion es: ', multiplicacion)
    print('La division es: ', division)

main()